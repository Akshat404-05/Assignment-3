import math
num = int(input("Enter a number : "))        #takes input from user
square_root = math.sqrt(num)                 #calculates the square root of the number
logarithm = math.log(num)                    #calculates the logarithm of the number
sine = math.sin(num)                         #calculates the sine of the number
print(f"Square root : {square_root}")        #printing the square root
print(f"Logarithm : {logarithm}")            #printing the logarithm
print(f"Sine : {sine}")                      #printing the sine value